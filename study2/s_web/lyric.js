
我用铅笔画了一幅画
画中有个美丽的她
乌黑齐肩的头发
清水般的明眸洗尽铅华
我看她总是不开心
就加个微翘的嘴巴
难以琢磨她的想法
真让我头大

木子申昜

历史的碧川源远流长
停在了戊戌上
有一个可爱的姑娘
名木子申昜

齐肩秀发美丽脸庞
恻引我目光
明眸皓齿神怡心旷
不见便心慌

希望总能遇到她
遇到却紧张
哑口无言的伫立
唯有漠装腔

听闻她将要远去
不知到何方
一想到从此便远扬
诚寸断肝肠

百代过客之滥觞
忽忽若所亡
兰草终究难遇上
吾将欲何往

吾将忍心洗污浊
铸造新模样
人我众生寿者相
皆于她身上

为什么上帝关上门
却没打开窗
不知道还有没有希望
留她在我身旁





























